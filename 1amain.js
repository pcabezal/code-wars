function dataReverse(data) {
    let arr = [];
    let concat = data.join('');
    let i = 0;
    while (i < concat.length) {
        arr.unshift(concat.slice(i, i+8) )
        i += 8;
    }
    return arr.join('').split('').map(e => +e  );
}

